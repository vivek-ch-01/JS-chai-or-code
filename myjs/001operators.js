// Arithmrtic operator---
let a = 10;
let b = 4;
console.log("a+b = ", a+b)

console.log("a**b = ",a**b)  // it meanse a to the power b

console.log("a%b = ",a%b)    // it means the reminder when dividing a from b.

console.log("a++ = ",a++)   // a= 10,  and here output of a++ is 10
console.log("a = ",a)       //here output of a is 11

console.log("++a = ",++a)   //here output of ++a is 12
console.log("a = ",a)       //here output of a is 12

// a++ means first the value of a is print.after that it is increased by 1.
// ++a means first the value of a is increased by 1 then after it print thatincreased value

