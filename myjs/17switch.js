// switch
const month = "march"

switch (month) {                        //to use switch case write first switch then select from sujestion it automatically form switch syntax
    case "janurary":
        console.log("janurary");
        
        break;
    case "februray":
        console.log("februray");
        
        break;
    case "march":
        console.log("march");
        
        break;
    case "April":
        console.log("April");
        
        break;

    default:
        console.log("default case match");
        
        break;
}